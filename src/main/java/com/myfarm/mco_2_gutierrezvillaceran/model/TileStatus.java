package com.myfarm.mco_2_gutierrezvillaceran.model;
/**
 * enumerates tile status
 */
public enum TileStatus {
    /**
     * tile is unplowed
     */
    UNPLOWED,
    /**
     * tile is plowed
     */
    PLOWED,
    /**
     * tile is planted with crop
     */
    PLANTED,
    /**
     * tile has withered plant
     */
    WITHERED,
    /**
     * tile has harvestable plant
     */
    HARVESTABLE,
    /**
     * tile has a rock
     */
    ROCK
}
